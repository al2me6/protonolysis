#[must_use]
/// From _Numerical Recipes_ 3rd. ed., `erfcc`, p. 265.
pub fn erfc(x: f64) -> f64 {
    let z = x.abs();
    let t = 2. / (2. + z);
    let ans = t
        * (-z * z - 1.265_512_23
            + t * (1.000_023_68
                + t * (0.374_091_96
                    + t * (0.096_784_18
                        + t * (-0.186_288_06
                            + t * (0.278_868_07
                                + t * (-1.135_203_98
                                    + t * (1.488_515_87
                                        + t * (-0.822_152_23 + t * 0.170_872_77)))))))))
            .exp();
    if x >= 0. {
        ans
    } else {
        2. - ans
    }
}
